#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define SIZE 100

struct Item {
    int key;        // ключ элемента
    char *info;     // указатель на информацию
};

struct Item table[SIZE];
int count = 0; // текущее количество элементов

// Проверка уникальности ключа
int findByKey(int key) {
    for (int i = 0; i < count; i++) {
        if (table[i].key == key) return i;
    }
    return -1;
}

// Добавление элемента
void addItem() {
    if (count >= SIZE) {
        printf("Таблица переполнена!\n");
        return;
    }
    int key;
    char buffer[256];
    printf("Введите ключ: ");
    scanf("%d", &key);
    if (findByKey(key) != -1) {
        printf("Ошибка: элемент с таким ключом уже существует!\n");
        return;
    }
    printf("Введите информацию: ");
    scanf(" %[^\n]", buffer);

    table[count].key = key;
    table[count].info = strdup(buffer);
    count++;
    printf("Элемент добавлен.\n");
}

// Удаление элемента
void deleteItem() {
    int key;
    printf("Введите ключ для удаления: ");
    scanf("%d", &key);
    int idx = findByKey(key);
    if (idx == -1) {
        printf("Элемент не найден.\n");
        return;
    }
    free(table[idx].info);
    for (int i = idx; i < count - 1; i++) {
        table[i] = table[i + 1];
    }
    count--;
    printf("Элемент удален.\n");
}

// Поиск по диапазону
void searchRange() {
    int minKey, maxKey;
    printf("Введите минимальный ключ: ");
    scanf("%d", &minKey);
    printf("Введите максимальный ключ: ");
    scanf("%d", &maxKey);

    printf("Результаты поиска:\n");
    for (int i = 0; i < count; i++) {
        if (table[i].key >= minKey && table[i].key <= maxKey) {
            printf("Ключ: %d, Инфо: %s\n", table[i].key, table[i].info);
        }
    }
}

// Вывод таблицы
void printTable() {
    if (count == 0) {
        printf("Таблица пуста.\n");
        return;
    }
    for (int i = 0; i < count; i++) {
        printf("%d: %s\n", table[i].key, table[i].info);
    }
}

// Главное меню
void menu() {
    int choice;
    do {
        printf("\n--- МЕНЮ ---\n");
        printf("1. Добавить элемент\n");
        printf("2. Удалить элемент\n");
        printf("3. Поиск по диапазону\n");
        printf("4. Показать таблицу\n");
        printf("0. Выход\n");
        printf("Ваш выбор: ");
        scanf("%d", &choice);

        switch (choice) {
            case 1: addItem(); break;
            case 2: deleteItem(); break;
            case 3: searchRange(); break;
            case 4: printTable(); break;
            case 0: break;
            default: printf("Неверный выбор!\n");
        }
    } while (choice != 0);
}

int main() {
    menu();
    // Очистка памяти
    for (int i = 0; i < count; i++) {
        free(table[i].info);
    }
    return 0;
}
